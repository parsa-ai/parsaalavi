interface AutoScrollerProps {
  skills: SkillItem[];
}

const AutoScroller = ({ skills }: AutoScrollerProps) => {
  const scrollRef = useRef<HTMLDivElement>(null);
  const animationFrameRef = useRef<number>();
  const isPaused = useRef(false); 

  const scrollWidth = useMemo(() => skills.length * 164, [skills.length]);

  const scroll = useCallback(() => {
    if (!scrollRef.current || isPaused.current) return;

    const { scrollLeft } = scrollRef.current;

    if (scrollLeft >= scrollWidth) {
      scrollRef.current.scrollLeft = 0;
    } else {
      scrollRef.current.scrollLeft += 2;
    }

    animationFrameRef.current = requestAnimationFrame(scroll);
  }, [scrollWidth]);

  useEffect(() => {
    animationFrameRef.current = requestAnimationFrame(scroll);
    return () => cancelAnimationFrame(animationFrameRef.current!);
  }, [scroll]);

  const handleMouseEnter = () => {
    isPaused.current = true;
    if (animationFrameRef.current) {
      cancelAnimationFrame(animationFrameRef.current);
    }
  };

  const handleMouseLeave = () => {
    isPaused.current = false;
    animationFrameRef.current = requestAnimationFrame(scroll);
  };

  const SkillCard = ({ item, index, isDuplicate = false }: {
    item: SkillItem;
    index: number;
    isDuplicate?: boolean;
  }) => (
    <div
      key={`${isDuplicate ? 'dup-' : ''}${item.title}-${index}`}
      className={`${item.color} h-12 min-w-36 flex items-center justify-center gap-2  rounded-xl bg-gradient-to-r from-slate-950  to-slate-950 bg-[length:200%_100%] bg-[20%]  transition-all duration-300 ease-out transform hover:scale-105   hover:bg-[50%] hover:backdrop-blur-sm hover:[transform:perspective(1000px)_translateZ(10px)]`}
      aria-hidden={isDuplicate}
    >
      <span className="text-sm font-medium text-white">{item.title}</span>
      <div className="w-4 h-4 flex items-center justify-center">{item.svg}</div>
    </div>
  );

  return (
    <div
      ref={scrollRef}
      className="mt-15 h-13  flex items-center w-full overflow-hidden select-none"
      onMouseEnter={handleMouseEnter}
      onMouseLeave={handleMouseLeave}
    >
      <div className="flex gap-5 whitespace-nowrap ">
        {skills.map((item, index) => (
          <SkillCard key={`skill-${index}`} item={item} index={index} />
        ))}
        {skills.map((item, index) => (
          <SkillCard key={`dup-${index}`} item={item} index={index} isDuplicate />
        ))}
      </div>
    </div>
  );
};






